#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	a;
	int	m;

	i = 0;
	m = 1;
	a = 0;
	while (str[i] == '\r' || str[i] == ' ' || str[i] == '\f'
			|| str[i] == '\v' || str[i] == '\t' || str[i] == '\n')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i++] == '-')
			m = -1;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		a = a * 10;
		a = a + str[i] - 48;
		i++;
	}
	return (a * m);
}
