#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*s;
	int		i;

	i = 0;
	while (src[i])
		i++;
	if (!(s = (char *)malloc(sizeof(char) * (i + 1))))
		return (NULL);
	i = 0;
	while (src[i])
	{
		s[i] = src[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
